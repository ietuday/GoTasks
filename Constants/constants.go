package main

func main() {
	// Declare a variable
	var name string
	// Assign a value to the variable
	name = "John Doe"
	// Print the variable
	println(name)

	// Declare a constant
	pi := 3.14159
	// Print the constant
	println(pi)
}

// This code demonstrates variable declaration and initialization in Go.
// It includes both explicit type declaration and short variable declaration.
